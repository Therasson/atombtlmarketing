    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $sector = Sector::find($id);

        if(!$sector)
        {
            return response([
                'message' => 'Sector not found.'
            ], 403);
        }

        $sector->delete();

        return response([
            'message' => 'Sector deleted.'
        ], 200);
    }
